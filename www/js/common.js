$.fn.Feedback = function (msg,type) {
    var container = $(this);
    if (type == "Success") { var color = "Green"; var imgsrc = "check"; }
    else if (type == "Error") { var color = "Red"; var imgsrc = "delete"; }
    else if (type == "Information") { var color = "Red"; var imgsrc = "info"; }
    else if (type == "Warning") { var color = "Red"; var imgsrc = "alert"; }
    container.empty()
        .append($('<a href="#" data-role="button" data-mini="true" data-icon="' + imgsrc + '" style="color:' + color + '" class="btn-wrap-text"></a>')
        .on("click", function () { $(this).fadeOut("slow", function () { container.empty(); }); })
        .append(msg)).trigger('create');
};
function CreateFeedbackDiv(msg, type) {
    var div = $('<div></div>');
    div.Feedback(msg, type);
    return div;
}
function AlertPopup(msg) {
    var btn = new Object();
	btn['Ok'] = { click: function () { }, icon: "check" };
    $(document).simpledialog({
        'mode': 'bool',
        'prompt': "<div style='padding:5px;'>" + msg + "</div>",
        'buttons': btn
    });
}
